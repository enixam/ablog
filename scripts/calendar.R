github_data <- function(from = NULL, user = "qiushiyan") {
  if (is.null(from)) {
    url <- str_glue("https://github.com/users/{user}/contributions")
  } else {
    url <- str_glue("https://github.com/users/{user}/contributions?from={from}")
  }

  html <- xml2::read_html(url)

  calendar <- html_node(html, ".js-calendar-graph-svg")
  calendar_days <- html_nodes(calendar, "rect")
  gh_data <- tibble(
    date = as.Date(html_attr(calendar_days, "data-date")),
    count = html_attr(calendar_days, "data-count"),
    type = "github",
    tip = str_glue("{count} contributions"),
    link = str_glue("https://github.com/{user}?from={date}&to={date}&tab=overview"),
  )
  gh_data
}

post_data <- function() {
  posts <- list.files(here::here("posts"), recursive = TRUE, full.names = FALSE, pattern = "^index[.]q?md")
  post_data <- future_map_dfr(set_names(posts, dirname(posts)), function(file) {
    data <- rmarkdown::yaml_front_matter(here::here("posts", file))
    list(
      date = as.Date(data$date),
      title = data$title
    )
  }, .id = "dir") %>%
    mutate(
      type = "post",
      link = file.path("post", dir),
      tip = title
    )
  post_data
}


cran_pkg_data <- function(author = "qiushiyan", pkgs = c("agua")) {
  pkg_search("Qiushi Yan") |>
    as_tibble() |>
    filter(package %in% pkgs) |>
    mutate(
      type = "pkg",
      link = str_glue("https://CRAN.R-project.org/package={package}"),
      tip = str_glue("{package}: {version}"),
      date = as.Date(date),
    )
}

fetch_repo <- function(repo) {
  url <- str_glue("https://api.github.com/repos/qiushiyan/{repo}")
  data <- GET(url, authenticate(Sys.getenv("GITHUB_CALENDAR_USER"), Sys.getenv("GITHUB_CALENDAR_PASSWORD"))) |> content()
  tibble(
    type = "project",
    link = url,
    tip = data$description,
    date = as.Date(data$created_at),
  )
}


